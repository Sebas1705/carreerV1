const monthName = (date: Date, lang: Language): string => {
    const months = MONTHS_LONG[lang] || MONTHS_LONG.en;
    return months[new Date(date).getMonth()] || MONTHS_LONG.en[new Date(date).getMonth()];
};

export function formatDateRange(start: Date, end: Date | undefined, lang: Language, currentLabel: string): string {
    const startStr = `${monthName(start, lang)} ${new Date(start).getFullYear()}`;
    const endStr = end ? `${monthName(end, lang)} ${new Date(end).getFullYear()}` : currentLabel;
    return `${startStr} - ${endStr}`;
}

export function formatMonthYear(date: Date, lang: Language): string {
    return `${monthName(date, lang)} ${new Date(date).getFullYear()}`;
}
